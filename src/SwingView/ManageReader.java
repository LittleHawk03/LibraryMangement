    public void update() {
        try {
            Reader reader = new Reader();
            reader.setId(Integer.parseInt(Id_Modify.getText()));
            reader.setFullName(name_modify.getText());
            reader.setGender(gender_Modify.getText());
            reader.setAddress(Address_Modify.getText());
            reader.setYearOfBirth(Integer.parseInt(year_modify.getText()));
            reader.setPhone(phone_modify.getText());
            reader.setClas(class_Modify.getText());
            reader.setEmp_Id(Integer.parseInt(empid_modify.getText()));
            if (readerDAO.update(reader)) {

                JOptionPane.showMessageDialog(this, "update succeccful");
                Object[] obj = {reader.getId(), reader.getFullName(), reader.getYearOfBirth(), reader.getGender(), reader.getAddress(), reader.getPhone(), reader.getClas(), reader.getEmp_Id()};
                model1 = (DefaultTableModel) rSTableMetro1.getModel();
                model1.addRow(obj);
            } else {
                JOptionPane.showMessageDialog(this, "update dont successfull");
            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }


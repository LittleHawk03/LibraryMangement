    public void searchReader() {

        try {
            Reader reader = readerDAO.search(Integer.parseInt(IdSearch.getText()));

            if (reader != null) {
                Id_Modify.setText(Integer.toString(reader.getId()));
                name_modify.setText(reader.getFullName());
                gender_Modify.setText(reader.getGender());
                Address_Modify.setText(reader.getAddress());
                phone_modify.setText(reader.getPhone());
                class_Modify.setText(reader.getClas());
                year_modify.setText(Integer.toString(reader.getYearOfBirth()));
                empid_modify.setText(Integer.toString(reader.getEmp_Id()));

                Object[] obj = {reader.getId(), reader.getFullName(), reader.getGender(), reader.getAddress(), reader.getPhone(), reader.getClas(), reader.getYearOfBirth(), reader.getEmp_Id()};

                model1 = (DefaultTableModel) rSTableMetro1.getModel();
                deleteRow();
                model1.addRow(obj);

            } else if (!name_search.getText().equals("")) {
                ResultSet rs = readerDAO.searchByName(name_search.getText());
                if (rs != null) {
                    deleteRow();
                    while (rs.next()) {
                        String id = rs.getString(1);
                        String fullName = rs.getString(2);
                        int year = rs.getInt(3);
                        String gender = rs.getString(4);
                        String Address = rs.getString(5);
                        String phone = rs.getString(6);
                        String clas = rs.getString(7);
                        int emp_id = rs.getInt(8);

                        Object[] obj = {id, fullName, year, gender, Address, phone, clas, emp_id};
                        model1 = (DefaultTableModel) rSTableMetro1.getModel();
                        model1.addRow(obj);
                    }

                }

            } else {
                JOptionPane.showMessageDialog(this, "cant find the reader");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "invalid data");
            e.printStackTrace();
        }

    }

    public void deleteRow() {
        int n = model1.getRowCount();
        for (int i = n - 1; i >= 0; i--) {
            model1.removeRow(i);
        }
    }

